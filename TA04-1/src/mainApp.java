
public class mainApp {
	
	public static void main(String[] args) {
		
		int valor1 = 46;
		int valor2 = 12;
		int resultado;
		
		resultado = valor1 + valor2;
		System.out.println("La suma de " + valor1 + " + " + valor2 + " es de: " + resultado);
		
		resultado = valor1 - valor2;
		System.out.println("La resta de " + valor1 + " - " + valor2 + " es de: " + resultado);
		
		resultado = valor1 * valor2;
		System.out.println("La multiplicación de " + valor1 + " x " + valor2 + " es de: " + resultado);
		
		resultado = valor1 / valor2;
		System.out.println("La división de " + valor1 + " / " + valor2 + " es de: " + resultado);
		
		resultado = valor1 % valor2;
		System.out.println("El resto de " + valor1 + " / " + valor2 + " es de: " + resultado);
		
	}
}
